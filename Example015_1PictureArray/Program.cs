void PrintImageArray(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) // универсальная запись вывода строк
    {
        for (int j = 0; j < image.GetLength(1); j++) // универсальная запись вывода колонок
        {
            // Console.Write($"{image[i, j]} ");
            if (image[i, j] == 0) Console.Write($" "); // <- печатаем "пробел"
            else Console.Write($"*");
        }
        Console.WriteLine(); // особенность вывода двумерного массива
    }
}
void FillImage(int row, int col) // заполнение двумерного массива
{
    if (picture[row, col] == 0)
    {
        picture[row, col] = 1;
        FillImage(row - 1, col); // Рекурсия, метод вызывает сам себя.
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}

